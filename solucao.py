def ler_arquivo(nome_arquivo):
    """
    Lê o arquivo de entrada e cria o estado inicial.

    Formato esperado:
    - ⚪️ : espaço vazio
    - 🧱 : barreira
    - 🤖 : agente
    - 📦(n) : caixa com peso n
    - 🎯 : alvo

    Args:
        nome_arquivo (str): Caminho para o arquivo de entrada

    Returns:
        Estado: Estado inicial do problema
    """
    with open(nome_arquivo, 'r', encoding='utf-8') as f:
        linhas = [linha.strip().split() for linha in f.readlines()]

    agente = None
    caixas = []
    alvos = []
    barreiras = []

    for y, linha in enumerate(linhas):
        for x, celula in enumerate(linha):
            if celula == '🤖':
                agente = (x, y)
            elif celula.startswith('📦'):
                # Extrai peso da caixa (formato: 📦(5))
                peso = int(celula[2:-1])  # Remove '📦(' e ')'
                caixas.append((x, y, peso))
            elif celula == '🎯':
                alvos.append((x, y))
            elif celula == '🧱':
                barreiras.append((x, y))
            # ⚪️ é ignorado (vazio)

    if agente is None:
        raise ValueError("Arquivo não contém agente (🤖)")

    return Estado(agente, caixas, alvos, barreiras)


def eh_posicao_valida(x, y, estado):
    """
    Verifica se uma posição é válida para movimento.

    Args:
        x, y (int): Coordenadas
        estado (Estado): Estado atual

    Returns:
        bool: True se a posição é válida (dentro do grid e não é barreira)
    """
    # Verifica limites do grid (assume grid retangular baseado nas barreiras)
    if estado.barreiras:
        max_x = max(max(x for x, _ in estado.barreiras), estado.agente[0],
                   max((x for x, _, _ in estado.caixas), default=0),
                   max((x for x, _ in estado.alvos), default=0)) + 2
        max_y = max(max(y for _, y in estado.barreiras), estado.agente[1],
                   max((y for _, y, _ in estado.caixas), default=0),
                   max((y for _, y in estado.alvos), default=0)) + 2
    else:
        # Se não há barreiras, usa posições das caixas/alvos para definir limites
        max_x = max(estado.agente[0],
                   max((x for x, _, _ in estado.caixas), default=0),
                   max((x for x, _ in estado.alvos), default=0)) + 2
        max_y = max(estado.agente[1],
                   max((y for _, y, _ in estado.caixas), default=0),
                   max((y for _, y in estado.alvos), default=0)) + 2

    if x < 0 or y < 0 or x >= max_x or y >= max_y:
        return False

    return (x, y) not in estado.barreiras


def encontrar_caixa(posicao, caixas):
    """
    Encontra uma caixa em determinada posição.

    Args:
        posicao (tuple): (x, y) a verificar
        caixas (list): Lista de caixas [(x, y, peso)]

    Returns:
        tuple or None: A caixa (x, y, peso) se encontrada, None caso contrário
    """
    for caixa in caixas:
        if (caixa[0], caixa[1]) == posicao:
            return caixa
    return None


def heuristica(estado):
    """
    Calcula a heurística para um estado.

    A heurística é a soma das distâncias de Manhattan de cada caixa
    ao alvo mais próximo, multiplicada pelo peso da caixa.

    Args:
        estado (Estado): Estado atual

    Returns:
        int: Valor heurístico (sempre ≤ custo real)
    """
    custo_estimado = 0
    alvos_disponiveis = estado.alvos.copy()

    # Ordena caixas por peso (mais pesadas primeiro - melhora a heurística)
    caixas_ordenadas = sorted(estado.caixas, key=lambda c: -c[2])

    for x_caixa, y_caixa, peso in caixas_ordenadas:
        if not alvos_disponiveis:
            break

        # Calcula distância para todos os alvos disponíveis
        distancias = []
        for x_alvo, y_alvo in alvos_disponiveis:
            dist = abs(x_caixa - x_alvo) + abs(y_caixa - y_alvo)
            distancias.append((dist, (x_alvo, y_alvo)))

        # Encontra o alvo mais próximo
        if distancias:
            dist_min, alvo_escolhido = min(distancias, key=lambda x: x[0])

            # Adiciona ao custo estimado
            custo_estimado += dist_min * peso

            # Remove o alvo escolhido
            alvos_disponiveis.remove(alvo_escolhido)

    return custo_estimado


def calcular_custo_acao(estado_anterior, estado_novo):
    """
    Calcula o custo de uma ação baseado nas caixas movidas.

    Args:
        estado_anterior (Estado): Estado antes da ação
        estado_novo (Estado): Estado depois da ação

    Returns:
        int: Custo da ação (1 para movimento simples, 1+peso para empurrar)
    """
    # Verifica se alguma caixa foi movida
    for i, (x_ant, y_ant, peso_ant) in enumerate(estado_anterior.caixas):
        x_novo, y_novo, _ = estado_novo.caixas[i]
        if (x_ant, y_ant) != (x_novo, y_novo):
            return 1 + peso_ant  # Custo com caixa

    return 1  # Custo de movimento simples


def gerar_sucessores(estado):
    """
    Gera todos os estados sucessores válidos a partir do estado atual.

    Args:
        estado (Estado): Estado atual

    Returns:
        list: Lista de tuplas (estado_sucessor, tipo_acao, peso_caixa)
               tipo_acao: 'mover' ou 'empurrar'
               peso_caixa: 0 para movimento, peso da caixa para empurrar
    """
    sucessores = []
    direcoes = [(-1, 0), (1, 0), (0, -1), (0, 1)]  # cima, baixo, esq, dir

    x_agente, y_agente = estado.agente

    for dx, dy in direcoes:
        nova_x = x_agente + dx
        nova_y = y_agente + dy

        # Verifica se a nova posição é válida
        if not eh_posicao_valida(nova_x, nova_y, estado):
            continue

        # Verifica se há caixa na nova posição
        caixa = encontrar_caixa((nova_x, nova_y), estado.caixas)

        if caixa:
            # Movimento com caixa (empurrar)
            caixa_x, caixa_y, peso = caixa
            nova_caixa_x = caixa_x + dx
            nova_caixa_y = caixa_y + dy

            # Verifica se a posição atrás da caixa é válida
            if not eh_posicao_valida(nova_caixa_x, nova_caixa_y, estado):
                continue

            # Verifica se não há outra caixa atrás
            if encontrar_caixa((nova_caixa_x, nova_caixa_y), estado.caixas):
                continue

            # Cria novo estado com a caixa movida
            novo_estado = estado.copiar()
            novo_estado.agente = (nova_x, nova_y)

            # Atualiza posição da caixa
            novas_caixas = []
            for cx, cy, cp in novo_estado.caixas:
                if (cx, cy) == (caixa_x, caixa_y):
                    novas_caixas.append((nova_caixa_x, nova_caixa_y, cp))
                else:
                    novas_caixas.append((cx, cy, cp))
            novo_estado.caixas = novas_caixas

            sucessores.append((novo_estado, 'empurrar', peso))

        else:
            # Movimento simples
            novo_estado = estado.copiar()
            novo_estado.agente = (nova_x, nova_y)
            sucessores.append((novo_estado, 'mover', 0))

    return sucessores


def eh_estado_objetivo(estado):
    """
    Verifica se um estado é objetivo (todas as caixas em alvos).

    Args:
        estado (Estado): Estado a verificar

    Returns:
        bool: True se todas as caixas estão em alvos
    """
    # Se número de caixas diferente de número de alvos, não pode ser objetivo
    if len(estado.caixas) != len(estado.alvos):
        return False

    # Verifica se cada caixa está em um alvo
    posicoes_caixas = {(x, y) for x, y, _ in estado.caixas}
    return all(pos in estado.alvos for pos in posicoes_caixas)


def reconstruir_caminho(veio_de, estado_final):
    """
    Reconstrói o caminho de estados do início até o objetivo.

    Args:
        veio_de (dict): Dicionário mapeando estado -> (estado_anterior, acao, peso)
        estado_final (Estado): Estado objetivo

    Returns:
        list: Lista de estados no caminho
    """
    caminho = []
    estado_atual = estado_final

    while estado_atual in veio_de:
        caminho.append(estado_atual)
        estado_atual, _, _ = veio_de[estado_atual]

    caminho.append(estado_atual)  # Adiciona estado inicial
    return list(reversed(caminho))

