def dijkstra(estado_inicial, max_iter=100000):
    """
    Algoritmo de Dijkstra (custo uniforme).

    Args:
        estado_inicial (Estado): Estado inicial
        max_iter (int): Número máximo de iterações

    Returns:
        tuple: (custo, estado_final, stats) onde stats é um dicionário com
               métricas de desempenho
    """
    print("Executando Dijkstra...")
    inicio = time.time()

    # Fronteira: (custo_acumulado, contador, estado)
    contador = 0
    fronteira = [(0, contador, estado_inicial)]
    heapq.heapify(fronteira)

    # Dicionários para controle
    custos = {estado_inicial: 0}
    veio_de = {}  # estado -> (estado_anterior, acao, peso)
    visitados = set()

    iteracoes = 0
    max_fronteira = 1

    while fronteira and iteracoes < max_iter:
        iteracoes += 1
        custo_atual, _, estado_atual = heapq.heappop(fronteira)

        # Atualiza estatísticas
        max_fronteira = max(max_fronteira, len(fronteira))

        # Se já visitamos este estado com custo menor, ignora
        if estado_atual in visitados:
            continue

        visitados.add(estado_atual)

        # Verifica se é objetivo
        if eh_estado_objetivo(estado_atual):
            fim = time.time()
            stats = {
                'iteracoes': iteracoes,
                'visitados': len(visitados),
                'max_fronteira': max_fronteira,
                'tempo': fim - inicio
            }
            return custo_atual, estado_atual, stats

        # Gera sucessores
        for sucessor, acao, peso in gerar_sucessores(estado_atual):
            custo_acao = 1 if acao == 'mover' else 1 + peso
            novo_custo = custo_atual + custo_acao

            if sucessor not in custos or novo_custo < custos[sucessor]:
                custos[sucessor] = novo_custo
                contador += 1
                heapq.heappush(fronteira, (novo_custo, contador, sucessor))
                veio_de[sucessor] = (estado_atual, acao, peso)

    fim = time.time()
    stats = {
        'iteracoes': iteracoes,
        'visitados': len(visitados),
        'max_fronteira': max_fronteira,
        'tempo': fim - inicio
    }
    return float('inf'), None, stats


def ganancioso(estado_inicial, max_iter=100000):
    """
    Algoritmo Ganancioso (Best-First Search).

    Args:
        estado_inicial (Estado): Estado inicial
        max_iter (int): Número máximo de iterações

    Returns:
        tuple: (custo, estado_final, stats) onde stats é um dicionário com
               métricas de desempenho
    """
    print("Executando Ganancioso...")
    inicio = time.time()

    # Fronteira: (heuristica, contador, estado)
    contador = 0
    fronteira = [(heuristica(estado_inicial), contador, estado_inicial)]
    heapq.heapify(fronteira)

    # Dicionários para controle
    veio_de = {}  # estado -> (estado_anterior, acao, peso)
    visitados = set()

    # Para calcular custo no final
    custos = {estado_inicial: 0}

    iteracoes = 0
    max_fronteira = 1

    while fronteira and iteracoes < max_iter:
        iteracoes += 1
        h_atual, _, estado_atual = heapq.heappop(fronteira)

        # Atualiza estatísticas
        max_fronteira = max(max_fronteira, len(fronteira))

        if estado_atual in visitados:
            continue

        visitados.add(estado_atual)

        # Verifica se é objetivo
        if eh_estado_objetivo(estado_atual):
            # Reconstrói caminho para calcular custo real
            custo_real = custos[estado_atual]
            fim = time.time()
            stats = {
                'iteracoes': iteracoes,
                'visitados': len(visitados),
                'max_fronteira': max_fronteira,
                'tempo': fim - inicio
            }
            return custo_real, estado_atual, stats

        # Gera sucessores
        for sucessor, acao, peso in gerar_sucessores(estado_atual):
            if sucessor not in visitados:
                # Calcula custo para este caminho
                custo_acao = 1 if acao == 'mover' else 1 + peso
                custos[sucessor] = custos[estado_atual] + custo_acao

                contador += 1
                heapq.heappush(fronteira, (heuristica(sucessor), contador, sucessor))
                veio_de[sucessor] = (estado_atual, acao, peso)

    fim = time.time()
    stats = {
        'iteracoes': iteracoes,
        'visitados': len(visitados),
        'max_fronteira': max_fronteira,
        'tempo': fim - inicio
    }
    return float('inf'), None, stats


def a_estrela(estado_inicial, max_iter=100000):
    """
    Algoritmo A*.

    Args:
        estado_inicial (Estado): Estado inicial
        max_iter (int): Número máximo de iterações

    Returns:
        tuple: (custo, estado_final, stats) onde stats é um dicionário com
               métricas de desempenho
    """
    print("Executando A*...")
    inicio = time.time()

    # Fronteira: (f = g + h, contador, estado)
    contador = 0
    f_inicial = 0 + heuristica(estado_inicial)
    fronteira = [(f_inicial, contador, estado_inicial)]
    heapq.heapify(fronteira)

    # Dicionários para controle
    g_custos = {estado_inicial: 0}  # custo real do caminho
    f_custos = {estado_inicial: f_inicial}
    veio_de = {}  # estado -> (estado_anterior, acao, peso)
    visitados = set()

    iteracoes = 0
    max_fronteira = 1

    while fronteira and iteracoes < max_iter:
        iteracoes += 1
        f_atual, _, estado_atual = heapq.heappop(fronteira)

        # Atualiza estatísticas
        max_fronteira = max(max_fronteira, len(fronteira))

        # Se já expandimos este estado, ignora
        if estado_atual in visitados:
            continue

        visitados.add(estado_atual)
        g_atual = g_custos[estado_atual]

        # Verifica se é objetivo
        if eh_estado_objetivo(estado_atual):
            fim = time.time()
            stats = {
                'iteracoes': iteracoes,
                'visitados': len(visitados),
                'max_fronteira': max_fronteira,
                'tempo': fim - inicio
            }
            return g_atual, estado_atual, stats

        # Gera sucessores
        for sucessor, acao, peso in gerar_sucessores(estado_atual):
            custo_acao = 1 if acao == 'mover' else 1 + peso
            novo_g = g_atual + custo_acao

            # Se encontrou caminho melhor para o sucessor
            if sucessor not in g_custos or novo_g < g_custos[sucessor]:
                g_custos[sucessor] = novo_g
                f = novo_g + heuristica(sucessor)
                f_custos[sucessor] = f

                contador += 1
                heapq.heappush(fronteira, (f, contador, sucessor))
                veio_de[sucessor] = (estado_atual, acao, peso)

    fim = time.time()
    stats = {
        'iteracoes': iteracoes,
        'visitados': len(visitados),
        'max_fronteira': max_fronteira,
        'tempo': fim - inicio
    }
    return float('inf'), None, stats


