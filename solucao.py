def salvar_resultado(nome_arquivo, algoritmo, estado_inicial, estado_final,
                     custo, stats, caminho_acoes=None):
    """
    Salva o resultado da execução em um arquivo.

    Args:
        nome_arquivo (str): Nome do arquivo de saída
        algoritmo (str): Nome do algoritmo usado
        estado_inicial (Estado): Estado inicial
        estado_final (Estado): Estado final (objetivo)
        custo (int): Custo total da solução
        stats (dict): Estatísticas da execução
        caminho_acoes (list): Lista de ações executadas
    """
    with open(nome_arquivo, 'w', encoding='utf-8') as f:
        f.write("=" * 60 + "\n")
        f.write(f"ALGORITMO: {algoritmo.upper()}\n")
        f.write("=" * 60 + "\n\n")

        f.write("ESTADO INICIAL:\n")
        f.write("-" * 40 + "\n")
        f.write(str(estado_inicial))
        f.write("\n\n")

        if estado_final:
            f.write("ESTADO FINAL (OBJETIVO):\n")
            f.write("-" * 40 + "\n")
            f.write(str(estado_final))
            f.write("\n\n")

            f.write("RESULTADOS:\n")
            f.write("-" * 40 + "\n")
            f.write(f"Custo total da solução: {custo}\n")
            f.write(f"Número de iterações: {stats['iteracoes']}\n")
            f.write(f"Estados visitados: {stats['visitados']}\n")
            f.write(f"Tamanho máximo da fronteira: {stats['max_fronteira']}\n")
            f.write(f"Tempo de execução: {stats['tempo']:.4f} segundos\n")

            if caminho_acoes:
                f.write(f"\nSEQUÊNCIA DE AÇÕES ({len(caminho_acoes)} passos):\n")
                for i, (acao, peso) in enumerate(caminho_acoes, 1):
                    if acao == 'mover':
                        f.write(f"  {i}. Mover agente (custo 1)\n")
                    else:
                        f.write(f"  {i}. Empurrar caixa de peso {peso} (custo {1+peso})\n")
        else:
            f.write("NENHUMA SOLUÇÃO ENCONTRADA!\n")
            f.write(f"Limite de iterações: {stats['iteracoes']}\n")
            f.write(f"Estados visitados: {stats['visitados']}\n")
            f.write(f"Tempo de execução: {stats['tempo']:.4f} segundos\n")


def gerar_arquivos_saida(estado_inicial, resultados):
    """
    Gera os três arquivos de saída (dijkstra.txt, ganancioso.txt, a_estrela.txt).

    Args:
        estado_inicial (Estado): Estado inicial do problema
        resultados (dict): Dicionário com resultados de cada algoritmo
    """
    for algoritmo in ['dijkstra', 'ganancioso', 'a_estrela']:
        if algoritmo in resultados:
            custo, estado_final, stats, caminho = resultados[algoritmo]
            nome_arquivo = f"{algoritmo}.txt"
            salvar_resultado(
                nome_arquivo, algoritmo, estado_inicial,
                estado_final, custo, stats, caminho
            )
            print(f"Arquivo gerado: {nome_arquivo}")


