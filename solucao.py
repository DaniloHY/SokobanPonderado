# CLASSE ESTADO

class Estado:
    """
    Representa um estado do jogo Sokoban.

    Atributos:
        agente (tuple): Posição (x, y) do agente
        caixas (list): Lista de tuplas (x, y, peso) para cada caixa
        alvos (list): Lista de tuplas (x, y) com posições dos alvos
        barreiras (set): Conjunto de tuplas (x, y) com posições das barreiras
    """

    def __init__(self, agente, caixas, alvos, barreiras):
        self.agente = tuple(agente)
        self.caixas = [(x, y, p) for x, y, p in caixas]
        self.alvos = [tuple(a) for a in alvos]
        self.barreiras = set(tuple(b) for b in barreiras)

        # Cache para hash (otimização)
        self._hash = None
        self._caixas_tuple = None

    def __hash__(self):
        """Hash baseado na posição do agente e posições das caixas."""
        if self._hash is None:
            # Ordena as caixas para hash consistente
            caixas_pos = sorted((x, y) for x, y, _ in self.caixas)
            self._caixas_tuple = tuple(caixas_pos)
            self._hash = hash((self.agente, self._caixas_tuple))
        return self._hash

    def __eq__(self, other):
        """Compara dois estados (ignora pesos para igualdade de posição)."""
        if not isinstance(other, Estado):
            return False

        if self.agente != other.agente:
            return False

        # Compara apenas posições das caixas, não os pesos
        self_pos = {(x, y) for x, y, _ in self.caixas}
        other_pos = {(x, y) for x, y, _ in other.caixas}
        return self_pos == other_pos

    def __lt__(self, other):
        """Necessário para usar heapq com estados."""
        return False  # Não usado diretamente

    def __str__(self):
        """Cria representação visual do grid."""
        # Encontra dimensões do grid
        max_x = max(
            self.agente[0],
            max((x for x, _, _ in self.caixas), default=0),
            max((x for x, _ in self.alvos), default=0),
            max((x for x, _ in self.barreiras), default=0)
        ) + 1

        max_y = max(
            self.agente[1],
            max((y for _, y, _ in self.caixas), default=0),
            max((y for _, y in self.alvos), default=0),
            max((y for _, y in self.barreiras), default=0)
        ) + 1

        # Cria grid vazio
        grid = [['⚪️' for _ in range(max_x)] for _ in range(max_y)]

        # Adiciona barreiras
        for x, y in self.barreiras:
            if 0 <= y < max_y and 0 <= x < max_x:
                grid[y][x] = '🧱'

        # Adiciona alvos
        for x, y in self.alvos:
            if 0 <= y < max_y and 0 <= x < max_x:
                grid[y][x] = '🎯'

        # Adiciona caixas (sobrescreve alvos se necessário)
        for x, y, peso in self.caixas:
            if 0 <= y < max_y and 0 <= x < max_x:
                if (x, y) in self.alvos:
                    grid[y][x] = f'✅'  # Caixa no alvo
                else:
                    grid[y][x] = f'📦({peso})'

        # Adiciona agente (sobrescreve se necessário)
        x, y = self.agente
        if 0 <= y < max_y and 0 <= x < max_x:
            if (x, y) in [pos for pos, _, _ in self.caixas]:
                grid[y][x] = '🤖📦'  # Agente sobre caixa (não deveria acontecer)
            else:
                grid[y][x] = '🤖'

        # Converte para string
        return '\n'.join(' '.join(linha) for linha in grid)

    def copiar(self):
        """Cria uma cópia profunda do estado."""
        return Estado(
            self.agente,
            [(x, y, p) for x, y, p in self.caixas],
            self.alvos[:],
            self.barreiras.copy()
        )

